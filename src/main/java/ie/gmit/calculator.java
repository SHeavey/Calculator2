package ie.gmit;

public class calculator {
    static int firstNum = 4;
    static int secondNum = 0;

    // This method will add two numbers
    static int Addsum(int firstNum,int secondNum) {
        return firstNum+secondNum;
    }

    static int SubtractSum(int firstNum,int secondNum) {
        return firstNum-secondNum;
    }

    static int MultSum(int firstNum,int secondNum) {
        return firstNum*secondNum;
    }

    static int DivSum(int firstNum,int secondNum) {
        return firstNum*secondNum;
    }


}
